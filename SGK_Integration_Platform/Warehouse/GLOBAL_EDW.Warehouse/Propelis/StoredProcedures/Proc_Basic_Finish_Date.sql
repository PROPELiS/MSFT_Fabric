        [Basic Finish Current Fiscal Year Flag],
        [Basic Finish Current Fiscal Year Minus 1 Flag],
        [Basic Finish Current Fiscal Year Minus 2 Flag],
        [Basic Finish Current Calendar Year Plus 1 Flag],
        [CURR_FISCAL_YR_PLUS_1_FLG],
