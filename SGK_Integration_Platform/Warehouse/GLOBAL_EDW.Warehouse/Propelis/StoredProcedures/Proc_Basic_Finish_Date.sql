        [DATE_KEY],
        [Basic Finish Date],
        [Basic Finish Fiscal Year],
        [Basic Finish Fiscal Period Number],
        [Basic Finish Fiscal Period Code],
        [Basic Finish Fiscal Quarter Number],
        [Basic Finish Fiscal Quarter Code],
        [Basic Finish Calendar Year],
        [Basic Finish Calendar Month Number],
        [Basic Finish Calendar Month Code],
        [Basic Finish Calendar Month Description],
        [Basic Finish Calendar Quarter Number],
        [Basic Finish Calendar Quarter Code],
        [Basic Finish Calendar Weekday Number],
        [Basic Finish Calendar Weekday Name],
        [Basic Finish Calendar Day In Month],
        [Basic Finish Calendar Day In Year],
        [Basic Finish Calendar Week],
        [Basic Finish Calendar Month In Quarter Number],
        [Basic Finish Calendar Days In Month],
        [ETL_SRC_SYS_CD],
        [ETL_CREATED_TS],
        [ETL_UPDTD_TS],
        [Basic Finish Current Day Flag],
        [Basic Finish Current Day Minus 1 Flag],
        [Basic Finish Current Calendar Week Flag],
        [Basic Finish Current Calendar Week Minus 1 Flag],
        [Basic Finish Current Month Flag],
        [Basic Finish Current Month Minus 1 Flag],
