CREATE   PROCEDURE [Propelis].[Proc_PLANNING_PLANT]
AS
BEGIN
    -- Update existing records
    UPDATE T
    SET 
        T.[Planning Plant ID] = S.[PLNT_ID],
        T.[Legacy Planning Plant ID] = S.[LGCY_PLNT_ID],
        T.[Planning Plant Name] = S.[PLNT_NAME],
        T.[Planning Plant Company Code] = S.[CMPNY_CD],
        T.[Planning Plant Sales Organization] = S.[SALES_ORGZTN],
        T.[Planning Plant Purchasing Organization] = S.[PURNG_ORGZTN],
        T.[Planning Plant Valuation Area] = S.[VALTN_AREA],
        T.[Planning Plant Customer Number] = S.[CUST_NUM_OF_PLNT],
        T.[Planning Plant Vendor Number] = S.[VNDR_NUM_OF_PLNT],
        T.[Planning Plant Factory Calendar Key] = S.[FACTORY_CLNDR_KEY],
        T.[Planning Plant Line 1 Description] = S.[PLNT_LINE_1_DESC],
        T.[Planning Plant Line 2 Description] = S.[PLNT_LINE_2_DESC],
        T.[Planning Plant City Name] = S.[PLNT_CITY_NAME],
        T.[Planning Plant Zip Code] = S.[PLNT_ZIP_CD],
        T.[Planning Plant Country Code] = S.[PLNT_CNTRY_CD],
        T.[Planning Plant Country Name] = S.[PLNT_CNTRY_NAME],
        T.[Planning Plant Tax Jurisdiction] = S.[TAX_JRSDCTN],
        T.[ETL SRC SYS CD] = S.[ETL_SRC_SYS_CD],
        T.[ETL EFFECTV BEGIN DATE] = S.[ETL_EFFECTV_BEGIN_DATE],
        T.[ETL EFFECTV END DATE] = S.[ETL_EFFECTV_END_DATE],
        T.[ETL CURR RCD IND] = S.[ETL_CURR_RCD_IND],
        T.[ETL CREATED TS] = S.[ETL_CREATED_TS],
        T.[ETL UPDTD TS] = S.[ETL_UPDTD_TS],
        T.[Planning Plant IS P001] = S.[IS_P001],
        T.[Planning Plant IS P002] = S.[IS_P002],
        T.[Planning Plant Company Description] = S.[CMPNY_DESC],
        T.[Planning Plant Sales Organization Description] = S.[SALES_ORG_DESC],
        T.[Planning Plant Factory Calendar Description] = S.[FACTORY_CLNDR_DESC],
        T.[Planning Plant Purchasing Organization Description] = S.[PURNG_ORGZTN_DESC],
        T.[Planning Plant Tax Indicator] = S.[PLNT_TAX_IND],
        T.[Planning Plant Tax Indicator Description] = S.[PLNT_TAX_IND_DESC],
        T.[Planning Plant Shipping Receiving Point] = S.[SHIPG_RECVNG_PNT],
        T.[Planning Plant Shipping Receiving Point Description] = S.[SHIPG_RECVNG_PNT_DESC]
    FROM [GLOBAL_EDW].[Propelis].[PLANNING_PLANT] AS T
    INNER JOIN [GLOBAL_EDW_MIRROR].[dbo].[EDW_T_D_MST_PLANT_CUR_D] AS S
        ON T.[PLNT_KEY] = S.[PLNT_KEY];

    -- Insert new records
    INSERT INTO [GLOBAL_EDW].[Propelis].[PLANNING_PLANT] 
    (
        [PLNT_KEY],
        [Planning Plant ID],
        [Legacy Planning Plant ID],
        [Planning Plant Name],
        [Planning Plant Company Code],
        [Planning Plant Sales Organization],
        [Planning Plant Purchasing Organization],
        [Planning Plant Valuation Area],
        [Planning Plant Customer Number],
        [Planning Plant Vendor Number],
        [Planning Plant Factory Calendar Key],
        [Planning Plant Line 1 Description],
        [Planning Plant Line 2 Description],
        [Planning Plant City Name],
        [Planning Plant Zip Code],
        [Planning Plant Country Code],
        [Planning Plant Country Name],
        [Planning Plant Tax Jurisdiction],
        [ETL SRC SYS CD],
        [ETL EFFECTV BEGIN DATE],
        [ETL EFFECTV END DATE],
        [ETL CURR RCD IND],
        [ETL CREATED TS],
        [ETL UPDTD TS],
        [Planning Plant IS P001],
        [Planning Plant IS P002],
        [Planning Plant Company Description],
        [Planning Plant Sales Organization Description],
        [Planning Plant Factory Calendar Description],
        [Planning Plant Purchasing Organization Description],
        [Planning Plant Tax Indicator],
        [Planning Plant Tax Indicator Description],
        [Planning Plant Shipping Receiving Point],
        [Planning Plant Shipping Receiving Point Description]
    )
    SELECT 
        S.[PLNT_KEY],
        S.[PLNT_ID],
        S.[LGCY_PLNT_ID],
        S.[PLNT_NAME],
        S.[CMPNY_CD],
        S.[SALES_ORGZTN],
        S.[PURNG_ORGZTN],
        S.[VALTN_AREA],
        S.[CUST_NUM_OF_PLNT],
        S.[VNDR_NUM_OF_PLNT],
        S.[FACTORY_CLNDR_KEY],
        S.[PLNT_LINE_1_DESC],
        S.[PLNT_LINE_2_DESC],
        S.[PLNT_CITY_NAME],
        S.[PLNT_ZIP_CD],
        S.[PLNT_CNTRY_CD],
        S.[PLNT_CNTRY_NAME],
        S.[TAX_JRSDCTN],
        S.[ETL_SRC_SYS_CD],
        S.[ETL_EFFECTV_BEGIN_DATE],
        S.[ETL_EFFECTV_END_DATE],
        S.[ETL_CURR_RCD_IND],
        S.[ETL_CREATED_TS],
        S.[ETL_UPDTD_TS],
        S.[IS_P001],
        S.[IS_P002],
        S.[CMPNY_DESC],
        S.[SALES_ORG_DESC],
        S.[FACTORY_CLNDR_DESC],
        S.[PURNG_ORGZTN_DESC],
        S.[PLNT_TAX_IND],
        S.[PLNT_TAX_IND_DESC],
        S.[SHIPG_RECVNG_PNT],
        S.[SHIPG_RECVNG_PNT_DESC]
    FROM [GLOBAL_EDW_MIRROR].[dbo].[EDW_T_D_MST_PLANT_CUR_D]AS S
    LEFT JOIN [GLOBAL_EDW].[Propelis].[PLANNING_PLANT] AS T
        ON T.[PLNT_KEY] = S.[PLNT_KEY]
    WHERE T.[PLNT_KEY] IS NULL;
END;