        S.[ETL_CURR_RCD_IND],
        S.[ETL_CREATED_TS],
        S.[ETL_UPDTD_TS],
        S.[COST_CNTR_DESC],
        S.[ACTL_PRMRY_COST_LOCK_IND],
        S.[PLAN_PRMRY_COST_LOCK_IND],
        S.[BUS_AREA],
        S.[PLAN_REV_LOCK_IND],
        S.[PO_BOX],
        S.[STREET],
        S.[USR_RESPNSBLE],
        S.[COST_CNTR_SHORT_DESC],
        S.[CONTROLLING_AREA_DESC],
