        S.[FUNC_AREA],
        S.[FUNC_AREA_DESC],
        S.[TAX_JRSDCTN],
        S.[COST_CNTR_FUNC],
        S.[PLANT_DESC],
        S.[CREATED_ON],
        S.[COST_CNTR_LONG_DESC],
        S.[VALID_TO_DATE],
        S.[VALID_FROM_DATE]
    FROM [GLOBAL_EDW_MIRROR].[dbo].[EDW_T_D_MST_COSTCNTR_CUR_D] AS S
    LEFT JOIN [GLOBAL_EDW].[Propelis].[COST_CENTER] AS T
        ON T.[COST_CNTR_KEY] = S.[COST_CNTR_KEY]
    WHERE T.[COST_CNTR_KEY] IS NULL;
