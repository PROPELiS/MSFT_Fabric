        [DATE_KEY],
        [Created On Date],
        [Created On Fiscal Year],
        [Created On Fiscal Period Number],
        [Created On Fiscal Period Code],
        [Created On Fiscal Quarter Number],
        [Created On Fiscal Quarter Code],
        [Created On Calendar Year],
        [Created On Calendar Month Number],
        [Created On Calendar Month Code],
        [Created On Calendar Month Description],
        [Created On Calendar Quarter Number],
        [Created On Calendar Quarter Code],
        [Created On Calendar Weekday Number],
        [Created On Calendar Weekday Name],
        [Created On Calendar Day In Month],
        [Created On Calendar Day In Year],
        [Created On Calendar Week],
        [Created On Calendar Month In Quarter Number],
        [Created On Calendar Days In Month],
        [ETL_SRC_SYS_CD],
        [ETL_CREATED_TS],
        [ETL_UPDTD_TS],
        [Created On Current Day Flag],
        [Created On Current Day Minus 1 Flag],
        [Created On Current Calendar Week Flag],
        [Created On Current Calendar Week Minus 1 Flag],
        [Created On Current Month Flag],
        [Created On Current Month Minus 1 Flag],
        [Created On Current Month Minus 12 Flag],
        [Created On Current Quarter Flag],
        [Created On Current Quarter Minus 1 Flag],
        [Created On Current Quarter Minus 4 Flag],
        [Created On Current Calendar Year Flag],
        [Created On Current Calendar Year Minus 1 Flag],
        [Created On Current Calendar Year Minus 2 Flag],
        [Created On Current Fiscal Year Flag],
        [Created On Current Fiscal Year Minus 1 Flag],
        [Created On Current Fiscal Year Minus 2 Flag],
        [Created On Current Calendar Year Plus 1 Flag],
        [CURR_FISCAL_YR_PLUS_1_FLG],
        [Created On Day In Fiscal Year],
        [Created On Date Is Weekday],
        [Created On Fiscal Week],
        [Created On Current Fiscal Week Flag],
        [Created On Current Fiscal Week Minus 1 Flag],
        [Created On Fiscal Weekday Number],
        [Created On Fiscal Weekday Name],
        [Created On Current Month Minus 2 Flag],
        [Created On Current Fiscal Week Minus 2 Flag],
        [Created On Current Fiscal Week Minus 6 Flag],
        [Created On Current Fiscal Week Minus 3 Flag],
        [Created On Current Fiscal Week Minus 4 Flag],
        [Created On Current Fiscal Week Minus 5 Flag],
        [Created On Current Fiscal Week Minus 7 Flag]
    )
    SELECT
        S.[DATE_KEY],
        S.[THE_DATE],
        S.[FISCAL_YR],
        S.[FISCAL_PER_NUM],
        S.[FISCAL_PER_CD],
        S.[FISCAL_QTR_NUM],
        S.[FISCAL_QTR_CD],
        S.[CLNDR_YR],
        S.[CLNDR_MNTH_NUM],
        S.[CLNDR_MNTH_CD],
        S.[CLNDR_MNTH_DESC],
        S.[CLNDR_QTR_NUM],
        S.[CLNDR_QTR_CD],
        S.[CLNDR_WEEKDAY_NUM],
        S.[CLNDR_WEEKDAY_NAME],
        S.[CLNDR_DAY_IN_MNTH],
        S.[CLNDR_DAY_IN_YR],
        S.[CLNDR_WK],
        S.[CLNDR_MNTH_IN_QTR_NUM],
        S.[CLNDR_DAYS_IN_MNTH],
        S.[ETL_SRC_SYS_CD],
        S.[ETL_CREATED_TS],
        S.[ETL_UPDTD_TS],
        S.[CURR_DAY_FLG],
        S.[CURR_DAY_MINUS_1_FLG],
        S.[CURR_CLNDR_WK_FLG],
        S.[CURR_CLNDR_WK_MINUS_1_FLG],
