        T.[Created On Date Is Weekday] = S.[IS_WEEKDAY],
        T.[Created On Fiscal Week] = S.[FISCAL_WK],
        T.[Created On Current Fiscal Week Flag] = S.[CURR_FISCAL_WK_FLG],
        T.[Created On Current Fiscal Week Minus 1 Flag] = S.[CURR_FISCAL_WK_MINUS_1_FLG],
