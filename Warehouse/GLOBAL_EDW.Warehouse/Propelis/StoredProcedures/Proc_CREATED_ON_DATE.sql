        T.[Created On Current Calendar Week Flag] = S.[CURR_CLNDR_WK_FLG],
        T.[Created On Current Calendar Week Minus 1 Flag] = S.[CURR_CLNDR_WK_MINUS_1_FLG],
        T.[Created On Current Month Flag] = S.[CURR_MNTH_FLG],
