CREATE   PROCEDURE [Propelis].[Proc_CREATED_ON_DATE]
AS
BEGIN  
    ----------------------------------------------------------------------
    -- Update existing records
    ----------------------------------------------------------------------
    UPDATE T
    SET
        T.[DATE_KEY] = S.[DATE_KEY],
        T.[Created On Date] = S.[THE_DATE],
        T.[Created On Fiscal Year] = S.[FISCAL_YR],
        T.[Created On Fiscal Period Number] = S.[FISCAL_PER_NUM],
        T.[Created On Fiscal Period Code] = S.[FISCAL_PER_CD],
        T.[Created On Fiscal Quarter Number] = S.[FISCAL_QTR_NUM],
        T.[Created On Fiscal Quarter Code] = S.[FISCAL_QTR_CD],
        T.[Created On Calendar Year] = S.[CLNDR_YR],
        T.[Created On Calendar Month Number] = S.[CLNDR_MNTH_NUM],
        T.[Created On Calendar Month Code] = S.[CLNDR_MNTH_CD],
        T.[Created On Calendar Month Description] = S.[CLNDR_MNTH_DESC],
        T.[Created On Calendar Quarter Number] = S.[CLNDR_QTR_NUM],
        T.[Created On Calendar Quarter Code] = S.[CLNDR_QTR_CD],
        T.[Created On Calendar Weekday Number] = S.[CLNDR_WEEKDAY_NUM],
