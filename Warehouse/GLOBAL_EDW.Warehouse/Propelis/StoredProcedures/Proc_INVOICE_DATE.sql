        ON T.[DATE_KEY] = S.[DATE_KEY];


    ----------------------------------------------------------------------
    -- Insert new records
    ----------------------------------------------------------------------
    INSERT INTO [GLOBAL_EDW].[Propelis].[INVOICE_DATE] (
        [DATE_KEY],
        [Invoice Posting Date],
        [Invoice Posting Fiscal Year],
        [Invoice Posting Fiscal Period],
        [Invoice Posting Fiscal Period Code],
        [Invoice Posting Fiscal Quarter],
        [Invoice Posting Fiscal Quarter Code],
        [Invoice Posting Calendar Year],
        [Invoice Posting Calendar Month Number],
        [Invoice Posting Calendar Month Code],
        [Invoice Posting Calendar Month Desc],
        [Invoice Posting Calendar Quarter],
        [Invoice Posting Calendar Quarter Code],
        [Invoice Posting Calendar Weekday Number],
        [Invoice Posting Calendar Weekday Name],
        [Invoice Posting Calendar Day],
        [Invoice Posting Calendar Day in Year],
        [Invoice Posting Calendar Week],
        [Invoice Posting Calendar Month in Quarter Number],
        [Invoice Posting Calendar Days in Month],
        [ETL_SRC_SYS_CD],
