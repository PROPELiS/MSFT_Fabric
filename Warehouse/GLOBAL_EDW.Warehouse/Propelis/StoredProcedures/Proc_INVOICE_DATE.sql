        [Invoice Posting Current Calendar Week Minus 1 Flag],
        [Invoice Posting Current Month Flag],
        [Invoice Posting Current Month Minus 1 Flag],
        [Invoice Posting Current Month Minus 12 Flag],
        [Invoice Posting Current Quarter Flag],
        [Invoice Posting Current Quarter Minus 1 Flag],
        [Invoice Posting Current Quarter Minus 4 Flag],
        [Invoice Posting Current Calendar Year Flag],
        [Invoice Posting Current Calendar Year Minus 1 Flag],
        [Invoice Posting Current Calendar Year Minus 2 Flag],
        [Invoice Posting Current Fiscal Year Flag],
        [Invoice Posting Current Fiscal Year Minus 1 Flag],
        [Invoice Posting Current Fiscal Year Minus 2 Flag],
        [Invoice Posting Current Calendar Year Plus 1 Flag],
        [Invoice Posting Current Fiscal Year Plus 1 Flag],
        [Invoice Posting Day In Fiscal Year],
        [Invoice Posting Date Is Weekday],
        [Invoice Posting Fiscal Week],
        [Invoice Posting Current Fiscal Week Flag],
        [Invoice Posting Current Fiscal Week Minus 1 Flag],
        [Invoice Posting Fiscal Weekday Number],
        [Invoice Posting Fiscal Weekday Name],
        [Invoice Posting Current Month Minus 2 Flag],
        [Invoice Posting Current Fiscal Week Minus 2 Flag],
        [Invoice Posting Current Fiscal Week Minus 6 Flag],
        [Invoice Posting Current Fiscal Week Minus 3 Flag],
        [Invoice Posting Current Fiscal Week Minus 4 Flag],
        [Invoice Posting Current Fiscal Week Minus 5 Flag],
        [Invoice Posting Current Fiscal Week Minus 7 Flag]
    )
    SELECT
        S.[DATE_KEY],
        S.[THE_DATE],
        S.[FISCAL_YR],
        S.[FISCAL_PER_NUM],
        S.[FISCAL_PER_CD],
        S.[FISCAL_QTR_NUM],
        S.[FISCAL_QTR_CD],
        S.[CLNDR_YR],
        S.[CLNDR_MNTH_NUM],
        S.[CLNDR_MNTH_CD],
        S.[CLNDR_MNTH_DESC],
        S.[CLNDR_QTR_NUM],
        S.[CLNDR_QTR_CD],
        S.[CLNDR_WEEKDAY_NUM],
        S.[CLNDR_WEEKDAY_NAME],
        S.[CLNDR_DAY_IN_MNTH],
        S.[CLNDR_DAY_IN_YR],
        S.[CLNDR_WK],
        S.[CLNDR_MNTH_IN_QTR_NUM],
        S.[CLNDR_DAYS_IN_MNTH],
        S.[ETL_SRC_SYS_CD],
        S.[ETL_CREATED_TS],
        S.[ETL_UPDTD_TS],
        S.[CURR_DAY_FLG],
        S.[CURR_DAY_MINUS_1_FLG],
        S.[CURR_CLNDR_WK_FLG],
        S.[CURR_CLNDR_WK_MINUS_1_FLG],
        S.[CURR_MNTH_FLG],
        S.[CURR_MNTH_MINUS_1_FLG],
        S.[CURR_MNTH_MINUS_12_FLG],
        S.[CURR_QTR_FLG],
        S.[CURR_QTR_MINUS_1_FLG],
        S.[CURR_QTR_MINUS_4_FLG],
        S.[CURR_CLNDR_YR_FLG],
        S.[CURR_CLNDR_YR_MINUS_1_FLG],
        S.[CURR_CLNDR_YR_MINUS_2_FLG],
        S.[CURR_FISCAL_YR_FLG],
        S.[CURR_FISCAL_YR_MINUS_1_FLG],
        S.[CURR_FISCAL_YR_MINUS_2_FLG],
        S.[CURR_CLNDR_YR_PLUS_1_FLG],
        S.[CURR_FISCAL_YR_PLUS_1_FLG],
        S.[DAY_IN_FISCAL_YR],
        S.[IS_WEEKDAY],
        S.[FISCAL_WK],
        S.[CURR_FISCAL_WK_FLG],
        S.[CURR_FISCAL_WK_MINUS_1_FLG],
