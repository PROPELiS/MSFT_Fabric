        [Start Calendar Month Code],
        [Start Calendar Month Description],
        [Start Calendar Quarter Number],
        [Start Calendar Quarter Code],
        [Start Calendar Weekday Number],
        [Start Calendar Weekday Name],
        [Start Calendar Day In Month],
        [Start Calendar Day In Year],
        [Start Calendar Week],
        [Start Calendar Month In Quarter Number],
        [Start Calendar Days In Month],
        [ETL_SRC_SYS_CD],
        [ETL_CREATED_TS],
        [ETL_UPDTD_TS],
        [Start Current Day Flag],
        [Start Current Day Minus 1 Flag],
        [Start Current Calendar Week Flag],
        [Start Current Calendar Week Minus 1 Flag],
        [Start Current Month Flag],
        [Start Current Month Minus 1 Flag],
        [Start Current Month Minus 12 Flag],
        [Start Current Quarter Flag],
        [Start Current Quarter Minus 1 Flag],
        [Start Current Quarter Minus 4 Flag],
        [Start Current Calendar Year Flag],
        [Start Current Calendar Year Minus 1 Flag],
        [Start Current Calendar Year Minus 2 Flag],
        [Start Current Fiscal Year Flag],
        [Start Current Fiscal Year Minus 1 Flag],
        [Start Current Fiscal Year Minus 2 Flag],
