    SET
        T.[DATE_KEY] = S.[DATE_KEY],
        T.[Start Date] = S.[THE_DATE],
        T.[Start Fiscal Year] = S.[FISCAL_YR],
        T.[Start Fiscal Period Number] = S.[FISCAL_PER_NUM],
        T.[Start Fiscal Period Code] = S.[FISCAL_PER_CD],
        T.[Start Fiscal Quarter Number] = S.[FISCAL_QTR_NUM],
        T.[Start Fiscal Quarter Code] = S.[FISCAL_QTR_CD],
        T.[Start Calendar Year] = S.[CLNDR_YR],
        T.[Start Calendar Month Number] = S.[CLNDR_MNTH_NUM],
        T.[Start Calendar Month Code] = S.[CLNDR_MNTH_CD],
        T.[Start Calendar Month Description] = S.[CLNDR_MNTH_DESC],
        T.[Start Calendar Quarter Number] = S.[CLNDR_QTR_NUM],
        T.[Start Calendar Quarter Code] = S.[CLNDR_QTR_CD],
        T.[Start Calendar Weekday Number] = S.[CLNDR_WEEKDAY_NUM],
        T.[Start Calendar Weekday Name] = S.[CLNDR_WEEKDAY_NAME],
        T.[Start Calendar Day In Month] = S.[CLNDR_DAY_IN_MNTH],
