        [Start Fiscal Weekday Name],
        [Start Current Month Minus 2 Flag],
        [Start Current Fiscal Week Minus 2 Flag],
        [Start Current Fiscal Week Minus 6 Flag],
        [Start Current Fiscal Week Minus 3 Flag],
        [Start Current Fiscal Week Minus 4 Flag],
        [Start Current Fiscal Week Minus 5 Flag],
        [Start Current Fiscal Week Minus 7 Flag]
    )
    SELECT
        S.[DATE_KEY],
        S.[THE_DATE],
        S.[FISCAL_YR],
        S.[FISCAL_PER_NUM],
        S.[FISCAL_PER_CD],
        S.[FISCAL_QTR_NUM],
        S.[FISCAL_QTR_CD],
        S.[CLNDR_YR],
        S.[CLNDR_MNTH_NUM],
        S.[CLNDR_MNTH_CD],
        S.[CLNDR_MNTH_DESC],
