        T.[Start Current Month Flag] = S.[CURR_MNTH_FLG],
        T.[Start Current Month Minus 1 Flag] = S.[CURR_MNTH_MINUS_1_FLG],
        T.[Start Current Month Minus 12 Flag] = S.[CURR_MNTH_MINUS_12_FLG],
        T.[Start Current Quarter Flag] = S.[CURR_QTR_FLG],
        T.[Start Current Quarter Minus 1 Flag] = S.[CURR_QTR_MINUS_1_FLG],
        T.[Start Current Quarter Minus 4 Flag] = S.[CURR_QTR_MINUS_4_FLG],
        T.[Start Current Calendar Year Flag] = S.[CURR_CLNDR_YR_FLG],
        T.[Start Current Calendar Year Minus 1 Flag] = S.[CURR_CLNDR_YR_MINUS_1_FLG],
        T.[Start Current Calendar Year Minus 2 Flag] = S.[CURR_CLNDR_YR_MINUS_2_FLG],
